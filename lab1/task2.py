# 2. Стратегия защитника: Оптимальное распределение ресурсов направлено на уравнивание ожидаемого ущерба.
# 3. Равновесие Штакельберга: Использовано LP для минимизации потерь против рационального атакующего.
# 4. Анализ бюджета: Бюджет B=300 ограничивает возможность защиты всех узлов.
# 5. Эффективность: Найден гарантированный максимум потерь в данных условиях.

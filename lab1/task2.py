import numpy as np
from scipy.optimize import linprog

# Фиксация seed для воспроизводимости
np.random.seed(42)

# 1. Параметры сети
N = 6  # Количество узлов (N >= 5)
B = 300  # Бюджет защитника

# Ценность узлов (Vi), стоимость защиты (Ci)
V = np.random.randint(50, 201, size=N)
C = np.random.randint(50, 151, size=N)

# Вероятности успеха атаки
P_succ_def = 0.2     # С защитой
P_succ_no_def = 0.9  # Без защиты

print("--- Параметры узлов ---")
for i in range(N):
    print(f"Узел {i}: Ценность={V[i]}, Стоимость защиты={C[i]}")
print(f"Бюджет защитника: {B}\n")

# 4. Поиск равновесия Штакельберга через Линейное Программирование
# Цель: минимизировать ожидаемый ущерб D.
# Атакующий выбирает узел i, максимизирующий: Vi * [ (1-xi)*P_no_def + xi*P_def ]
# Где xi - вероятность защиты узла i.
# Обозначим Vi_eff = Vi * (P_no_def - P_def).
# Ожидаемый ущерб на узле i: Vi*P_no_def - xi * Vi_eff.
# Задача: min D при D >= Vi*P_no_def - xi*Vi_eff для всех i,
# и ограничениях: sum(xi * Ci) <= B, 0 <= xi <= 1.

# Вектор переменных: [x0, x1, ..., xN-1, D]
c_lp = np.zeros(N + 1)
c_lp[-1] = 1  # Минимизируем D

# Ограничения ущерба: Vi*P_no_def - xi*Vi_eff <= D  =>  -xi*Vi_eff - D <= -Vi*P_no_def
Vi_eff = V * (P_succ_no_def - P_succ_def)
A_ub = np.zeros((N + 1, N + 1))
for i in range(N):
    A_ub[i, i] = -Vi_eff[i]
    A_ub[i, N] = -1

b_ub = -V * P_succ_no_def

# Ограничение бюджета: sum(xi * Ci) <= B
A_ub[N, :N] = C
b_ub = np.append(b_ub, B)

# Границы переменных: 0 <= xi <= 1, D >= 0
bounds = [(0, 1)] * N + [(0, None)]

res = linprog(c_lp, A_ub=A_ub, b_ub=b_ub, bounds=bounds, method='highs')

if res.success:
    strategies = res.x[:N]
    expected_damage = res.x[N]

    print("--- Результаты (Равновесие Штакельберга) ---")
    for i in range(N):
        print(f"Вероятность защиты узла {i}: {strategies[i]:.4f}")
    print(f"\nМинимальный ожидаемый ущерб: {expected_damage:.4f}")
else:
    print("Ошибка оптимизации")


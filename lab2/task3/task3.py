def plot_state_distribution(P: list, k: int, n: int, out_path: str) -> None:
    """Распределение предельных вероятностей состояний."""
    fig, ax = plt.subplots(figsize=(12, 5.5))
    xs = list(range(len(P)))
    # Цвет столбцов: серый для P_0, синий для занятых каналов, оранжевый
    # для состояний с очередью, красный для отказа
    colors = []
    for i in xs:
        if i == 0:
            colors.append("lightgray")
        elif i < k:
            colors.append("steelblue")
        elif i == k:
            colors.append("orange")
        elif i < k + n:
            colors.append("darkorange")
        else:
            colors.append("crimson")
    ax.bar(xs, P, color=colors, edgecolor="navy", alpha=0.85)

    # Подписи значений над столбцами
    for i, p in enumerate(P):
        if p > max(P) * 0.005:
            label = fmt_mid(p) if p >= 1e-4 else f"{p:.1e}"
            ax.text(i, p + max(P) * 0.01, label,
                    ha="center", va="bottom", fontsize=8)

    ax.axvline(k - 0.5, color="gray", linestyle="--", alpha=0.5)
    ax.axvline(k + 0.5, color="orange", linestyle="--", alpha=0.6)
    ax.set_xlabel("Состояние i (число заявок в системе)")
    ax.set_ylabel("Предельная вероятность P_i")
    ax.set_xticks(xs)
    ax.set_title(f"Распределение вероятностей состояний СМО M/M/k/n "
                 f"(k = {k}, n = {n})\n"
                 f"Серый — простой; синий — заняты каналы; "
                 f"оранжевый — очередь; красный — отказ")
    ax.grid(True, alpha=0.3, axis="y")
    fig.tight_layout()
    fig.savefig(out_path, dpi=120, bbox_inches="tight")
    plt.close(fig)


def plot_sensitivity_to_lambda(mu, k, n, T, C, lam_orig, out_path):
    """
    График зависимости основных показателей от интенсивности λ
    при фиксированных остальных параметрах.
    """
    lambdas = [0.5 * i for i in range(1, int(2 * k * mu) + 1)]
    P_otk, Q, A, S_loss = [], [], [], []
    for lam in lambdas:
        r = mmkn_characteristics(lam, mu, k, n)
        P_otk.append(r["P_otk"])
        Q.append(r["Q"])
        A.append(r["A"])
        S_loss.append(C * lam * r["P_otk"] * T)

    fig, axes = plt.subplots(1, 2, figsize=(13, 5))

    # Левая панель: P_отк, Q
    ax = axes[0]
    ax.plot(lambdas, P_otk, color="crimson", label="P_отк(λ)")
    ax.plot(lambdas, Q, color="seagreen", label="Q(λ)")
    ax.axvline(lam_orig, color="orange", linestyle=":",
               label=f"λ = {lam_orig} (задано)")
    ax.set_xlabel("Интенсивность потока λ, заявок/час")
    ax.set_ylabel("Вероятность")
    ax.set_title("Вероятность отказа и относительная\nпропускная способность")
    ax.grid(True, alpha=0.3)
    ax.legend()

    # Правая панель: A, S_loss
    ax = axes[1]
    ax.plot(lambdas, A, color="steelblue",
            label=f"A(λ), заявок/час (предел k·μ = {k * mu})")
    ax.axhline(k * mu, color="gray", linestyle="--", alpha=0.5)
    ax.set_xlabel("Интенсивность потока λ, заявок/час")
    ax.set_ylabel("A, заявок/час", color="steelblue")
    ax.tick_params(axis="y", labelcolor="steelblue")
    ax.grid(True, alpha=0.3)

    ax2 = ax.twinx()
    ax2.plot(lambdas, S_loss, color="crimson", linestyle="--",
             label="S_потерь(λ), у.е.")
    ax2.set_ylabel("Потери S_потерь, у.е.", color="crimson")
    ax2.tick_params(axis="y", labelcolor="crimson")

    ax.axvline(lam_orig, color="orange", linestyle=":", alpha=0.7)
    ax.set_title("Абсолютная пропускная способность\nи потери выручки")

    lines1, labels1 = ax.get_legend_handles_labels()
    lines2, labels2 = ax2.get_legend_handles_labels()
    ax.legend(lines1 + lines2, labels1 + labels2, loc="center right",
              fontsize=9)

    fig.suptitle(f"Зависимости показателей от λ при k = {k}, n = {n}, "
                 f"μ = {mu} заявок/час", fontsize=12, fontweight="bold")
    fig.tight_layout()
    fig.savefig(out_path, dpi=120, bbox_inches="tight")
    plt.close(fig)



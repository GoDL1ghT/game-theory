def print_state_table(P: list, k: int, n: int) -> None:
    """Таблица предельных вероятностей всех (k+n+1) состояний."""
    print(f"  {'Состояние':<18} | {'P_i':>16} | Комментарий")
    print(f"  {'-' * 18}-+-{'-' * 16}-+-{'-' * 25}")
    for i, p in enumerate(P):
        if i == 0:
            comment = "все каналы простаивают"
        elif i < k:
            comment = f"занято {i} канал(ов)"
        elif i == k:
            comment = "все каналы заняты, очередь пуста"
        elif i < k + n:
            comment = f"очередь: {i - k} заявок(а)"
        else:
            comment = "очередь заполнена → отказ"
        print(f"  P_{i:<16} | {fmt_sci(p):>16} | {comment}")
    s = sum(P)
    print(f"  {'Σ P_i':<18} | {fmt_mid(s):>16} | (контроль: должна быть 1)")


def print_characteristics(r: dict, lam: float, k: int, n: int) -> None:
    """Подробный отчёт о характеристиках обслуживания."""
    print()
    print("ОКОНЧАТЕЛЬНЫЕ ХАРАКТЕРИСТИКИ ОБСЛУЖИВАНИЯ (3 знака):")
    print(f"  Вероятность отказа                  P_отк   = "
          f"{fmt_fin(r['P_otk'])}    (≈ {fmt_sci(r['P_otk'])})")
    print(f"  Относительная пропускная способность Q     = "
          f"{fmt_fin(r['Q'])}")
    print(f"  Абсолютная пропускная способность   A      = "
          f"{fmt_fin(r['A'])} заявок/час")
    print(f"  Среднее число заявок в очереди      L_оч   = "
          f"{fmt_fin(r['L_q'])}")
    print(f"  Среднее число занятых каналов       k̄      = "
          f"{fmt_fin(r['k_busy'])}")
    print(f"  Среднее число заявок в системе      L_сист = "
          f"{fmt_fin(r['L'])}")
    # Времена удобнее в минутах
    print(f"  Среднее время ожидания              T_оч   = "
          f"{fmt_fin(r['T_q'] * 60)} мин")
    print(f"  Среднее время обслуживания          T_обс  = "
          f"{fmt_fin(r['T_serv'] * 60)} мин")
    print(f"  Среднее время в системе             T_сис  = "
          f"{fmt_fin(r['T_sys'] * 60)} мин")



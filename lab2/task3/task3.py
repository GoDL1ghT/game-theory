def main() -> None:
    # ---------- Исходные данные ----------
    lam = 6              # λ — заявок/час
    t_min = 2            # t — мин/заявка
    k = 12               # число каналов
    n = 3                # число мест в очереди
    T = 12               # часов работы СМО
    C = 90               # у.е./заявка

    # ---------- Перевод к единым единицам (часы) ----------
    # μ = 1/(t [час]) = 60/t (заявок/час)
    mu = 60.0 / t_min
    rho = lam / mu
    chi = rho / k

    print("ИСХОДНЫЕ ДАННЫЕ И РАСЧЁТНЫЕ ПАРАМЕТРЫ")
    print("-" * 72)
    print(f"  Интенсивность потока:                 λ = {lam} заявок/час")
    print(f"  Среднее время обслуживания:           t = {t_min} мин")
    print(f"  Число каналов:                        k = {k}")
    print(f"  Число мест в очереди:                 n = {n}")
    print(f"  Время работы СМО:                     T = {T} ч")
    print(f"  Стоимость одной заявки:               C = {C} у.е.")
    print()
    print(f"  Производные параметры:")
    print(f"     μ = 60/t                = {fmt_mid(mu)} заявок/час")
    print(f"     ρ = λ/μ                 = {fmt_mid(rho)}")
    print(f"     χ = ρ/k                 = {fmt_mid(chi)}  "
          f"(средняя загрузка одного канала)")
    print(f"     Максимум заявок в системе = k + n = {k + n}")

    # ---------- Задание 1. Анализ СМО ----------
    r = mmkn_characteristics(lam, mu, k, n)

    print("\n" + "=" * 72)
    print(f"  ЗАДАНИЕ 1. Анализ работы СМО")
    print("=" * 72)
    print(f"Вероятность простоя всех каналов: P_0 = {fmt_mid(r['P0'])}")
    print()
    print("Таблица 1. Предельные вероятности состояний (всего "
          f"{k + n + 1} состояний):")
    print_state_table(r["P"], k, n)

    print_characteristics(r, lam, k, n)

    # ---------- Задание 2. Потеря выручки ----------
    S_loss = C * lam * r["P_otk"] * T
    # Также полезно посчитать выручку фактическую
    S_revenue = C * r["A"] * T          # выручка с реально обслуженных заявок
    N_arrived = lam * T                 # сколько всего пришло
    N_lost = lam * r["P_otk"] * T       # сколько было отказано
    N_served = r["A"] * T               # сколько обслужено

    print("\n" + "=" * 72)
    print(f"  ЗАДАНИЕ 2. Потеря выручки из-за отказов")
    print("=" * 72)
    print(f"Формула:  S_потерь = C · λ · P_отк · T")
    print(f"Расчёт:   S_потерь = {C} · {lam} · {fmt_mid(r['P_otk'])} · {T}")
    print(f"          S_потерь = {fmt_mid(S_loss)} у.е.")
    print()
    print("Сводка по заявкам за время T:")
    print(f"  Всего поступило (λ·T):              {fmt_fin(N_arrived)} заявок")
    print(f"  Обслужено        (A·T):              {fmt_fin(N_served)} заявок")
    print(f"  Получили отказ   (λ·P_отк·T):        {fmt_fin(N_lost)} заявок")
    print(f"  Фактическая выручка (C·A·T):         {fmt_fin(S_revenue)} у.е.")
    print(f"  Потеря выручки   (C·λ·P_отк·T):      {fmt_fin(S_loss)} у.е.")

    # ---------- Графики ----------
    out_dir = os.path.dirname(os.path.abspath(__file__))
    g1 = os.path.join(out_dir, "task3_state_distribution.png")
    g2 = os.path.join(out_dir, "task3_sensitivity.png")
    plot_state_distribution(r["P"], k, n, g1)
    plot_sensitivity_to_lambda(mu, k, n, T, C, lam, g2)
    print(f"\nГрафики сохранены:")
    print(f"  • {g1}")
    print(f"  • {g2}")

    # ---------- Выводы ----------
    print("\n" + "=" * 72)
    print("  ВЫВОДЫ")
    print("=" * 72)
    print(
        f"1. Система существенно избыточна по числу каналов: ρ = "
        f"{fmt_mid(rho)} при k = {k}, поэтому загрузка одного канала "
        f"χ = ρ/k = {fmt_mid(chi)} ({fmt_fin(chi * 100)} %). "
        f"Практически вся вероятностная масса сосредоточена на состояниях "
        f"0–2; вероятности занятости даже половины каналов уже исчезающе "
        f"малы."
    )
    print(
        f"2. Вероятность отказа P_отк = {fmt_sci(r['P_otk'])} — "
        f"величина практически нулевая. Относительная пропускная "
        f"способность Q = {fmt_fin(r['Q'])} ≈ 1, абсолютная "
        f"A = {fmt_fin(r['A'])} ≈ λ = {lam} заявок/час: система "
        f"справляется почти со всеми заявками."
    )
    print(
        f"3. Среднее число заявок в очереди L_оч = {fmt_sci(r['L_q'])} "
        f"и среднее время ожидания T_оч = {fmt_sci(r['T_q'] * 60)} мин "
        f"также пренебрежимо малы. Среднее время в системе T_сис = "
        f"{fmt_fin(r['T_sys'] * 60)} мин ≈ среднему времени обслуживания "
        f"t = {t_min} мин — заявки практически не ждут."
    )
    print(
        f"4. Потеря выручки S_потерь = {fmt_fin(S_loss)} у.е. за T = {T} ч "
        f"работы — пренебрежимо мала по сравнению с фактической выручкой "
        f"{fmt_fin(S_revenue)} у.е."
    )
    print(
        f"5. Анализ чувствительности (см. task3_sensitivity.png): при росте "
        f"λ потери начинают быть заметными лишь после λ ≈ k·μ = "
        f"{k * mu} заявок/час, когда поток приближается к суммарной "
        f"пропускной способности всех каналов. При снижении k или росте t "
        f"(увеличение ρ) ситуация меняется качественно: P_отк растёт, "
        f"L_оч растёт, S_потерь становится существенным."
    )
    print()
    print("ЗАМЕЧАНИЕ К ИСХОДНЫМ ДАННЫМ:")
    print(
        f"  При комбинации λ = 6 заявок/час и t = 2 мин система с k = {k} "
        f"каналами оказывается значительно переразмеренной (ρ/k ≈ "
        f"{chi * 100:.1f} %). Это математически корректный, но "
        f"вырожденный случай: оптимизация и потери практически отсутствуют. "
        f"Если t предполагалось в часах, нагрузка стала бы ρ = 12 при k = 12 "
        f"(χ = 1) — типичная задача с заметным процентом отказов; такой "
        f"перерасчёт занимает одну строчку (mu = 1.0/t_hour)."
    )


if __name__ == "__main__":
    main()

"""
Лабораторная работа по теории массового обслуживания.

Задача 3. Многоканальная СМО с ограниченной очередью (M/M/k/n).

Расчёт по формулам учебного пособия
М. А. Плескунова «Теория массового обслуживания» (2022).

Исходные данные:
    λ = 6    — интенсивность входного потока, заявок/час
    t = 2    — среднее время обслуживания одной заявки, минут
    k = 12   — число каналов обслуживания
    n = 3    — число мест в очереди (вместимость очереди)
    T = 12   — время работы СМО, часов
    C = 90   — стоимость обслуживания одной заявки, у.е.

Требуется:
    1) Анализ работы СМО:
       - предельные вероятности состояний;
       - вероятность отказа, относительная и абсолютная пропускная способность;
       - среднее число заявок в очереди, на обслуживании, в системе;
       - среднее время ожидания, обслуживания и пребывания в системе.
    2) Потеря выручки из-за отказов:
       S_потерь = C · λ · P_отк · T

Соглашение о точности (по методическим указаниям):
    • промежуточные вычисления — 5 знаков после запятой;
    • окончательные ответы     — 3 знака после запятой;
    • число состояний конечно (k + n + 1 = 16), ряды не бесконечные.
"""

import math
import os
import matplotlib.pyplot as plt


# Константы точности
PREC_MID = 5
PREC_FIN = 3


def fmt_mid(x: float) -> str:
    """Промежуточное число (5 знаков)."""
    return f"{x:.{PREC_MID}f}"


def fmt_fin(x: float) -> str:
    """Окончательный ответ (3 знака)."""
    return f"{x:.{PREC_FIN}f}"


def fmt_sci(x: float) -> str:
    """Очень маленькое число — в научной нотации."""
    if x == 0 or x >= 1e-4:
        return fmt_mid(x)
    return f"{x:.3e}"


# ----------------------------------------------------------------------
# Расчётные функции M/M/k/n
# ----------------------------------------------------------------------

def mmkn_characteristics(lam: float, mu: float, k: int, n: int) -> dict:
    """
    Полный расчёт характеристик СМО M/M/k/n
    (k каналов, n мест в очереди — конечная ёмкость k + n).

    Параметры
    ----------
    lam : λ — интенсивность входного потока, заявок/час;
    mu  : μ — интенсивность обслуживания одного канала, заявок/час;
    k   : число каналов;
    n   : число мест в очереди.

    Формулы (Плескунова, гл. «СМО с ограниченной очередью»):

      ρ = λ/μ,    χ = ρ/k

      Вероятность простоя P_0:
         P_0 = 1 / [ Σ_{i=0..k} ρ^i/i!  +  (ρ^k/k!) · χ(1-χ^n)/(1-χ) ]
         (при χ = 1 второе слагаемое равно (ρ^k/k!) · n)

      Вероятности состояний:
         P_i = (ρ^i / i!) · P_0,            i = 0..k    (нет очереди)
         P_i = ρ^i / (k! · k^(i-k)) · P_0,  i = k+1..k+n (есть очередь)

      Вероятность отказа:
         P_отк = P_{k+n} = ρ^(k+n) / (k! · k^n) · P_0

      Относительная пропускная способность:  Q = 1 - P_отк
      Абсолютная пропускная способность:     A = λ · Q
      Среднее число заявок в очереди:        L_оч = Σ_{j=1..n} j · P_{k+j}
      Среднее число занятых каналов:         k̄ = A / μ = ρ · Q
      Среднее число заявок в системе:        L_сист = L_оч + k̄

      Среднее время (формула Литтла, λ_эфф = λQ — поток обслуженных):
         T_оч  = L_оч  / λ_эфф
         T_обс = 1 / μ      (среднее время самого обслуживания)
         T_сис = T_оч + T_обс  ( = L_сист / λ_эфф )
    """
    rho = lam / mu
    chi = rho / k                                  # коэффициент загрузки канала

    # Сумма по состояниям без очереди: Σ ρ^i/i! для i = 0..k
    sum_serve = sum((rho ** i) / math.factorial(i) for i in range(k + 1))

    # Сумма по состояниям с очередью
    if abs(chi - 1) < 1e-12:
        # Особый случай χ = 1: формула суммы геометрической прогрессии вырождена
        sum_queue = (rho ** k) / math.factorial(k) * n
    else:
        sum_queue = ((rho ** k) / math.factorial(k)
                     * chi * (1 - chi ** n) / (1 - chi))

    P0 = 1.0 / (sum_serve + sum_queue)

    # Все вероятности состояний i = 0, 1, ..., k+n
    P = []
    for i in range(k + n + 1):
        if i <= k:
            pi = (rho ** i) / math.factorial(i) * P0
        else:
            pi = (rho ** i) / (math.factorial(k) * k ** (i - k)) * P0
        P.append(pi)

    # --- Основные характеристики ---
    P_otk = P[k + n]                                # вероятность отказа = P_{k+n}
    Q = 1.0 - P_otk                                 # относительная проп. способность
    A = lam * Q                                     # абсолютная проп. способность

    # Среднее число заявок в очереди
    L_q = sum(j * P[k + j] for j in range(1, n + 1))

    # Среднее число занятых каналов (по формуле A/μ = ρ·Q)
    k_busy = A / mu

    # Среднее число заявок в системе
    L = L_q + k_busy

    # Эффективный поток (обслуженных заявок)
    lam_eff = lam * Q

    # Времена (формула Литтла)
    T_q = L_q / lam_eff if lam_eff > 0 else 0.0     # ожидания в очереди
    T_serv = 1.0 / mu                                # обслуживания
    T_sys = T_q + T_serv                             # пребывания в системе

    return {
        "rho": rho, "chi": chi,
        "P0": P0, "P": P,
        "P_otk": P_otk, "Q": Q, "A": A,
        "L_q": L_q, "L": L, "k_busy": k_busy,
        "T_q": T_q, "T_serv": T_serv, "T_sys": T_sys,
        "lam_eff": lam_eff,
    }


